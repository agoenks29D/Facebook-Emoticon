function facebook_emoticon($text)
{
    preg_match_all ('#<span class="facebook_emoticon (.+?)" emoticon_data="(.+?)"></span>#', facebook_emoticon_list(), $emoticons);
    foreach ($emoticons[2] as $key => $value)
	{
		$replaces[$value] = '<span class="facebook_emoticon '.$emoticons[1][$key].'" emoticon_data="'.$value.'"></span>';
